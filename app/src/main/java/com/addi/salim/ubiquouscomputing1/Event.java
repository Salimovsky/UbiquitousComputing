package com.addi.salim.ubiquouscomputing1;

public class Event {
    final long time;
    final double x;
    final double y;
    final double z;

    public Event(long time, double x, double y, double z) {
        this.time = time;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
